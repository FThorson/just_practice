day = int(input('Enter a number 1 - 7: '))
#Monday = 1
#Tuesday = 2
#Wednesday = 3
#Thursday = 4
#Friday = 5
#Saturday = 6
#Sunday = 7
if day == 1:
    print('Monday')
elif day == 2:
    print('Tuesday')
elif day == 3:
    print('Wednesday')
elif day == 4:
    print('Thursday')
elif day == 5:
    print('Friday')
elif day == 6:
    print('Saturday')
else:
    print('Sunday')
if day > 7:
    print('Error: Must enter a value of 1, 2, 3, 4, 5, 6, or 7.')

    

    
